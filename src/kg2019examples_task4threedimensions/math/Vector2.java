package kg2019examples_task4threedimensions.math;

public class Vector2 {
    private final double p1;
    private final double p2;

    public Vector2(double p1, double p2) {
        this.p1 = p1;
        this.p2 = p2;
    }

    public double getP1() {
        return p1;
    }

    public double getP2() {
        return p2;
    }
}
